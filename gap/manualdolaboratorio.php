<h2>..::Como Instalar::..</h2>

<b>DEBIAN</b>:<br>
O Produto j&aacute; est&aacute; incluido na disbribui&ccedil;&atilde;o
Debian, bastando configurar corretamente os fontes do apt e instalar
o pacote planner. Em geral basta executar <i>$apt-get install planner</i> e aguardar
a instalação ser concluída.<br><br>

<b>Outras distribui&ccedil;&otilde;es/sabores de UNIX</b>:<br>
Primeiramente
buscar informa&ccedil;&otilde;es no site da sua distribui&ccedil;&atilde;o,
certamente h&aacute; uma maneira padronizada de instalar este
software, que deve estar dispon&iacute;vel pelo sistema gerenciador
de pacotes da mesma. <br><br>

Caso n&atilde;o
seja poss&iacute;vel fazer esta instala&ccedil;&atilde;o, faz-se
necess&aacute;rio compilar o Planner a partir dos fontes no site: <br><br>

<A HREF="http://ftp.gnome.org/pub/GNOME/sources/planner/">http://ftp.gnome.org/pub/GNOME/sources/planner/</A>
<br><br>

Compilar
de acordo com a sua distribui&ccedil;&atilde;o, atentando para as
depend&ecirc;ncias de bibliotecas.<br>
O planner faz parte do gerenciador de desktop Gnome, portanto parte
deste sistema se far&aacute; necess&aacute;ria para a instala&ccedil;&atilde;o
do planner.<br><br>

<b>WINDOWS</b>:<br>
Existe
um &ldquo;port&rdquo; do planner para windows, dispon&iacute;vel para
download em:<br><br>

<A HREF="http://winplanner.sourceforge.net/">http://winplanner.sourceforge.net/</A><br><br>

&Eacute; necess&aacute;ria a instala&ccedil;&atilde;o das bibliotecas do GTK,
como informado no site. Esta versão ainda não está comercializável.<br><br>

<b>IMPORTANTE</b>:<br>
O <b>Planner</b> &eacute; uma deriva&ccedil;&atilde;o do MRProject, de modo
que a maioria das instru&ccedil;&otilde;es e procedimentos executados
em um dos dois aplicativos pode ser executada de forma an&aacute;loga
no outro, de modo que os tutorais podem abranger as 2 ferramentas.

